public class Construtores {
    /*
        Construtores servem para deixar o código sem objetos vazios durante a execução.
        Obriga o programador a informar os dados atribuídos as varáveis.

        Por exemplo:

            public String name;
            public double price;
            public int quantity;

            public Product (String name, double price, int quantity) {
                this.name = name;
                this.price = price;
                this.quantity = quantity;

        Assim, quando o método Product for acionado, no mesmo momento as variáveis receberão suas atribiuições
    
    */
}
